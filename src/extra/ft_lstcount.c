#include "libft.h"

size_t ft_lstcount(t_list *lst)
{
	size_t i;
	t_list *tmp;

	i = 0;
	tmp = lst;
	if (tmp)
	{
		while (tmp)
		{
			tmp = tmp->next;
			i++;
		}
	}
	return (i);
}
